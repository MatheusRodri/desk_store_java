    private void registrarVenda(){
        try{
            String produtoBD = nome_produto.getText();
            double valorBD = Double.parseDouble(valor.getText());
            String dataBD = data.getText();

            venda = new Venda(produtoBD, valorBD, dataBD);
            dao.inserir(venda);
            
            JOptionPane.showConfirmDialog(rootPane, "Venda conclu√≠da com sucesso!");
        }     
        catch (NumberFormatException e) {
           JOptionPane.showMessageDialog(null, "Erro na venda!",
                    "Alerta!", JOptionPane.ERROR_MESSAGE);
        }
        
    }
