    private void ConfirmarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_ConfirmarMouseClicked
        if (nome_produto.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Digite o nome do produto!!",
                    "Alerta!", JOptionPane.ERROR_MESSAGE);
        } else if (valor.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Digite o valor do produto!!",
                    "Alerta!", JOptionPane.ERROR_MESSAGE);
        } else if (data.getText().equals("  /  /    ")) {
            JOptionPane.showMessageDialog(null, "Digite a data da venda do produto!!",
                    "Alerta!", JOptionPane.ERROR_MESSAGE);
        } else {
            String produtoBD = nome_produto.getText();
            double valorBD = Double.parseDouble(valor.getText());
            String dataBD = data.getText();

            Venda objVenda = new Venda();
            objVenda.setNome_produto(produtoBD);
            objVenda.setValor(valorBD);
            objVenda.setData_pagamento(dataBD);

            VendaDAO objVendaDAO = new VendaDAO();
            objVendaDAO.inserir(objVenda);

            JOptionPane.showMessageDialog(rootPane, "Venda conclu√≠da com sucesso!");
           

            if (atendente) {
                Tela_Menu menu = new Tela_Menu(true);
                menu.setVisible(true);
                this.dispose();
            } else {

                Tela_Menu menu = new Tela_Menu(false);
                menu.setVisible(true);
                this.dispose();
            }
        }
    }//GEN-LAST:event_ConfirmarMouseClicked

